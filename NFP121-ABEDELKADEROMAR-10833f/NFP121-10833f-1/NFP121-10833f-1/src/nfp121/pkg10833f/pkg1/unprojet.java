    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        List code  = new List();
        List text = new List();
        String Dir = System.getProperty("user.home"); // to get the user home directory of every desktop
        JFileChooser filchooser = new JFileChooser(); 
        filchooser.setCurrentDirectory(new java.io.File(Dir + "/Desktop"));
        filchooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY); // to show only directories
        int returnVal = filchooser.showSaveDialog(this);
        if(returnVal == JFileChooser.APPROVE_OPTION) { File yourFolder = filchooser.getSelectedFile();         

            File filedirectory = yourFolder;
            File[] files = filedirectory.listFiles();
            for(File file : files) { 
                String fileName = file.toString();
                String filePath = file.getPath(); 
                int index = fileName.lastIndexOf('.');
                if(index > 0) {
                    String extension = fileName.substring(index + 1);
                    if (extension == "txt")
                    {
                        
                        text.add(filePath);
                    }
                    else if (extension == "java")
                    {
                        code.add(filePath);
                    }
                }
            }      
        }
    }//GEN-LAST:event_jButton3ActionPerformed
